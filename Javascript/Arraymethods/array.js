


let sizes = [30,32,34];

console.log(sizes);

//push
sizes.push(22);
console.log(sizes);

//pop

sizes.pop()
console.log(sizes);


//shift
sizes.shift()
console.log(sizes);

//unshift
sizes.unshift(28)
console.log(sizes);

//splice
sizes.splice(2,0,25)
console.log(sizes);

//slice
console.log("Before Slice");
console.log(sizes);
console.log("after slice");
console.log(sizes);

//includes
console.log(sizes.includes(40));
console.log(sizes.includes(25));

//indexof
console.log(sizes.indexOf(20));
console.log(sizes.indexOf(32));


//reverse
console.log(sizes.reverse());






